package in.ajay.binding;

public class UnlockAccountForm {

	private String email;
	private String tempPsswd;
	private String newPsswd;
	private String confirmPsswd;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTempPsswd() {
		return tempPsswd;
	}
	public void setTempPsswd(String tempPsswd) {
		this.tempPsswd = tempPsswd;
	}
	public String getNewPsswd() {
		return newPsswd;
	}
	public void setNewPsswd(String newPsswd) {
		this.newPsswd = newPsswd;
	}
	public String getConfirmPsswd() {
		return confirmPsswd;
	}
	public void setConfirmPsswd(String confirmPsswd) {
		this.confirmPsswd = confirmPsswd;
	}
	
	
}
